package lk.my.sliit.it18106398.foodapp;

public class ModelOrder {
    private int image;
    private String name;

    public ModelOrder(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }

}
